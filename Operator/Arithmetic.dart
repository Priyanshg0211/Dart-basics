// Understanding arithmetic operator
/* The operator are 
   1. + ----> Use for addition 
   2. - ----> Use for subtraction 
   3. / ----> Use for division 
   4. * ----> Use for multiplication 
   5. % ----> Use for modulus 
   6. ~/ ----> Use for not (it basically divide if the answer is in decimal it wil give the whole value) */ 

void main() {
  var a = 30;
  var b = 20;
  print(a + b);
  print(a - b);
  print(a / b);
  print(a * b);
  print(a % b);
  print(a ~/ b);
}
